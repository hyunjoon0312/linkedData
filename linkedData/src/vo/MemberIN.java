package vo;

public class MemberIN {
	private String INname;
	private String INid;
	private String INpasswd;
	private String INemail;
	
	
	public String getINName() {
		return INname;
	}
	public void setINName(String INname) {
		this.INname = INname;
	}
	public String getINId() {
		return INid;
	}
	public void setINId(String INid) {
		this.INid = INid;
	}
	public String getINPasswd() {
		return INpasswd;
	}
	public void setINPasswd(String INpasswd) {
		this.INpasswd = INpasswd;
	}
	public String getINEmail() {
		return INemail;
	}
	public void setINEmail(String INemail) {
		this.INemail = INemail;
	}
}
