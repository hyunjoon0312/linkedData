package vo;

public class MemberSTAT {
	private String STATname;
	private String STATid;
	private String STATpasswd;
	private String STATemail;
	
	
	public String getSTATName() {
		return STATname;
	}
	public void setSTATName(String STATname) {
		this.STATname = STATname;
	}
	public String getSTATId() {
		return STATid;
	}
	public void setSTATId(String STATid) {
		this.STATid = STATid;
	}
	public String getSTATPasswd() {
		return STATpasswd;
	}
	public void setSTATPasswd(String STATpasswd) {
		this.STATpasswd = STATpasswd;
	}
	public String getSTATEmail() {
		return STATemail;
	}
	public void setSTATEmail(String STATemail) {
		this.STATemail = STATemail;
	}
}
