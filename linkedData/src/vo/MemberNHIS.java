package vo;

public class MemberNHIS {
	private String NHISname;
	private String NHISid;
	private String NHISpasswd;
	private String NHISemail;
	
	
	public String getNHISName() {
		return NHISname;
	}
	public void setNHISName(String NHISname) {
		this.NHISname = NHISname;
	}
	public String getNHISId() {
		return NHISid;
	}
	public void setNHISId(String NHISid) {
		this.NHISid = NHISid;
	}
	public String getNHISPasswd() {
		return NHISpasswd;
	}
	public void setNHISPasswd(String NHISpasswd) {
		this.NHISpasswd = NHISpasswd;
	}
	public String getNHISEmail() {
		return NHISemail;
	}
	public void setNHISEmail(String NHISemail) {
		this.NHISemail = NHISemail;
	}
}
